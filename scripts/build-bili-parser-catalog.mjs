import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(fileURLToPath(new URL('..', import.meta.url)));
const dataPath = resolve(ROOT, 'static/data/mv_bilibili.0.jsonl');
const outputFlag = process.argv.indexOf('--out');
const outputPath = outputFlag >= 0 ? resolve(process.cwd(), process.argv[outputFlag + 1] || '') : '';

if (!outputPath) {
  throw new Error('Usage: node scripts/build-bili-parser-catalog.mjs --out <catalog.mjs>');
}

const entries = (await readFile(dataPath, 'utf8')).split('\n')
  .filter((line) => line.startsWith('{'))
  .map((line) => JSON.parse(line))
  .filter((entry) => /^BV[0-9A-Za-z]{10}$/.test(entry.bilibili_bvid));

const pages = new Map();
for (const entry of entries) {
  const bvid = entry.bilibili_bvid;
  const page = Math.max(1, Number.parseInt(entry.bilibili_page, 10) || 1);
  if (!pages.has(bvid)) pages.set(bvid, page);
}

const source = [
  '// Generated by scripts/build-bili-parser-catalog.mjs. Do not edit manually.',
  'export const ALLOWED_MV_PAGES = new Map([',
  ...[...pages].map(([bvid, page]) => `  [${JSON.stringify(bvid)}, ${page}],`),
  ']);',
  '',
].join('\n');
await writeFile(outputPath, source, 'utf8');
console.log(`Wrote ${pages.size} BVIDs to ${outputPath}.`);
